public class Day4 {
    public static void main(String[] args) {
        System.out.print("Halo Nama \n Saya"); // (\n)new line (pindah ke baris baru)
        System.out.print("Yusnul \t magfira"); //(\t) tab (spasi horizontal yang terformat)
        System.out.println("Saya Tinggal Di\bTapango");//(\b) backspace (menghapus karakter sebelumnya pada output)
        System.out.println("Saya\r Jurusan Informatika"); //(\r) return (kembali ke awal baris)

    }
}
