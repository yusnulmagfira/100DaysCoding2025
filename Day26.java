package YusnulMagfira;

public class Day26 {
    public static void main(String[] args) {
        //operator penugasan

        int angka = 5;

        angka += 2;
        System.out.println("nilai angka : " + angka); //hasilnya 7

        angka -=2;
        System.out.println("nilai angka : " + angka); //hasilnya 5

        angka *=2;
        System.out.println("nilai angka : " + angka); //hasilnya 10

        angka /=2;
        System.out.println("nilai angka : " + angka); //hasilnya 5

        angka %=2;
        System.out.println("nilai angka : " + angka); //hasilnya 1
    }
}
